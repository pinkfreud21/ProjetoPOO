    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaCompra.getSelectedRow();
        tabelaCompra.clearSelection();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Jogo para remover do carrinho.");
        } else {
            Jogos jogoSelecionado = listaVendas.get(linhaSelecionada);
            listaVendas.remove(jogoSelecionado);
            atualizaTabela();
        }
        atualizaSaldo();
    }//GEN-LAST:event_jButton2ActionPerformed

