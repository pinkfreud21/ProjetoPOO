    public void visualizar() {
        jLabelSaldo.setVisible(false);
        jButton1.setVisible(false);
        jLabel4.setVisible(false);
        jButton4.setVisible(false);
        jButton2.setVisible(false);

        jButton3.setText("Fechar");

        Conexao con = new Conexao();
        try {
            Statement stmt = con.getConnection().createStatement();
            String sql = "SELECT j.nome AS nome_jogo, j.genero, j.preco FROM public.historico h JOIN public.jogos j ON h.idjogo = j.id WHERE h.separacao = " + separacao + ";";
            ResultSet rs = stmt.executeQuery(sql);
            Jogos j;
            while (rs.next()) {
                j = new Jogos();
                j.setNome(rs.getString(1));
                j.setGenero(rs.getString(2));
                j.setPreco(rs.getFloat(3));
                listaHistorico.add(j);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        atualizaTabelaHistorico();
        txtTotalCompra.setText(Float.toString(saldo));
    }
